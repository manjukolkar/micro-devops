Hi Welcome to git basic git commends
Command	Purpose
git clone <repo>	Copy a remote repository to your local machine
git status	Check the current state of the working directory
git add <file>	Stage changes for commit
git commit -m "message"	Save staged changes with a message
git push	Upload local commits to the remote repository
git pull	Fetch and merge changes from the remote repository
git fetch	Download updates without merging
git branch	List or create branches
git checkout <branch>	Switch to another branch
git switch <branch>	Modern command to switch branches
git merge <branch>	Merge another branch into the current branch
git rebase <branch>	Reapply commits on top of another branch
git log	View commit history
git diff	Show code differences
git stash	Temporarily save uncommitted changes
git restore <file>	Discard changes in a file
git reset	Undo commits or unstage changes
